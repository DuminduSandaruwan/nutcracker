
<h2>Nutcracker Bug/Ehancement process</h2>
<h3>
Nutcracker code is open source and is hosted on github.

Here is the link to go and review or create bugs and enhancements against the nutcracker software
<br/>
<a href=https://github.com/smeighan/nutcracker/issues>Nutcracker issue tracker</a>
<br/>

After you click this link
<ul>
<li>Click "New Request"</li>
<li>Click under "Add Label", RED for bug, BLUE for enhancement
</li>
<li>Enter your bug info</li></ul>
</h3>


<h2>Items if entering a bug</h2>
<ol>
<li> Your login name to the nutcracker software
<li> What effect name you are working on
<li> A description of the problem
</ol>
<br/>

<h2>Items if requesting an enhancement</h2>
<ol>
<li> Your login name to the nutcracker software
<li> A description of what enhancement you would like to see. If it is specific to an effect class, mention the class name
</ol>
